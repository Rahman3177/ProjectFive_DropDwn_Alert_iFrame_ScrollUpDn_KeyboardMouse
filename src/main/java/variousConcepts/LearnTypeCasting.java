package variousConcepts;

public class LearnTypeCasting {
	public static void main(String[] args) {
	
		//JavaTypeCasting:
		int a = 5;
		double b = a; //Widening Casting......Here we are converting Integer into Double 
		
		double x = 9.5;
		int y = (int) x; //Narrowing CastingHere we are converting Double into Integer 
		
		System.out.println(a);
		System.out.println(b);
		System.out.println(x);
		System.out.println(y);
		
		
	} 

}
